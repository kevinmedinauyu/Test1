// For of es ideal para iterar sobre arrays y extraer sus datos


let animales = ["Chanchito Feliz", "Dragon","Perro"];

for (let animal of animales){
    console.log(animal);
}

console.log("Bucle for terminado");

let i = 0;

while(i < animales.length){
    console.log(animales[i]);
    i++
}

    
